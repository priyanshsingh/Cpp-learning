vector<int> compareTriplets(vector<int> a, vector<int> b) {
    
    vector<int> arr;
    int point_a = 0;
    int point_b = 0;

    for(int i=0;i<a.size();i++){
        if(a[i] > b[i]) 
            point_a ++;
        else if(b[i] > a[i])
            point_b++;
        }         
        arr.push_back(point_a);
        arr.push_back(point_b);
    return arr;
}


